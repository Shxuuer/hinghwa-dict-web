import { message } from 'ant-design-vue'

export default {
  name: 'LayoutHeader',
  components: {
    headerUser
  },
  data () {
    return {
      searchContent: ''
    }
  },
  computed: {
    tab: {
      get () {
        return [this.$route.name]
      },
      set () {}
    }
  },
  methods: {
    search () {
      if (!this.searchContent) message.warning('请先输入搜索内容哦~')
      this.$router.push({
        name: 'Search',
        query: { key: this.searchContent }
      })
    }
  }
}
